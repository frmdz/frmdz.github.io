export function dibujarBloques() {
  NIVEL.forEach(row => {
    row.forEach(element => {
      if(element.state){
        context.fillStyle = (element.state == 2) ? "#bdbdbd" : "white";
        context.fillRect(element.x, element.y, 44, 17);
      }
    });
  });
}